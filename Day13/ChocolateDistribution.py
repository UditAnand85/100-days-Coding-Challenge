class Solution:
    def findMinDiff(self, arr, m):
        n = len(arr)
        if m == 0 or n == 0:
            return 0
        if n < m:
            return -1  # or handle as per problem constraints
        
        # Step 1: Sort the packets
        arr.sort()
        
        # Step 2: Use window of size m
        min_diff = float('inf')
        for i in range(n - m + 1):
            diff = arr[i + m - 1] - arr[i]
            min_diff = min(min_diff, diff)
            
        return min_diff
